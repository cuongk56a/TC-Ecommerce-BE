import express from 'express';
import { auth } from '../../middlewares/auth';
import {
    addCreatedByIdToBody,
    addDeletedByToBody,
    addUpdatedByIdToBody,
} from '../../middlewares/addUserToBody'
import {validate} from '../../middlewares/validate';
import {notificationController} from './option.controller';
import {notificationValidation} from './option.validation';

const router = express.Router()

router
    .route('/')
    .post(auth(),addCreatedByIdToBody,validate(notificationValidation.createOne),notificationController.createOne)
    .get(validate(notificationValidation.getList),notificationController.getList);

router.route('/all').get(auth(),validate(notificationValidation.getAll),notificationController.getAll);

router
    .route('/:notificationId')
    .get(validate(notificationValidation.getOne), notificationController.getOne)
    .patch(auth(), addUpdatedByIdToBody, validate(notificationValidation.updateOne), notificationController.updateOne)
    .delete(auth(), addDeletedByToBody, validate(notificationValidation.deleteOne), notificationController.deleteOne)

export const notificationRoute = router;

