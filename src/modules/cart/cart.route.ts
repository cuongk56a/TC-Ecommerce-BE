import express from 'express';
import { auth } from '../../middlewares/auth';
import {
    addCreatedByIdToBody,
    addDeletedByToBody,
    addUpdatedByIdToBody,
} from '../../middlewares/addUserToBody'
import {validate} from '../../middlewares/validate';
import {cartController} from './cart.controller';
import {cartValidation} from './cart.validation';

const router = express.Router()

router
    .route('/')
    .post(auth(),addCreatedByIdToBody,validate(cartValidation.createOne),cartController.createOne)
    .get(auth(),validate(cartValidation.getList),cartController.getList);

router.route('/all').get(auth(),validate(cartValidation.getAll),cartController.getAll);
router.route('/creat-or-update').get(auth(),validate(cartValidation.createOrUpdate),cartController.createOrUpdate);

router
    .route('/:cartId')
    .get(auth(),validate(cartValidation.getOne), cartController.getOne)
    .patch(auth(), addUpdatedByIdToBody, validate(cartValidation.updateOne), cartController.updateOne)
    .delete(auth(), addDeletedByToBody, validate(cartValidation.deleteOne), cartController.deleteOne)

export const cartRoute = router;

