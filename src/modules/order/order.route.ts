import express from 'express';
import { auth } from '../../middlewares/auth';
import {
    addCreatedByIdToBody,
    addDeletedByToBody,
    addUpdatedByIdToBody,
} from '../../middlewares/addUserToBody'
import {validate} from '../../middlewares/validate';
import {orderController} from './order.controller';
import {orderValidation} from './order.validation';
import { authIfHas } from '../../middlewares/authIfHas';

const router = express.Router()

router
    .route('/')
    .post(authIfHas(),addCreatedByIdToBody,validate(orderValidation.createOne),orderController.createOne)
    .get(validate(orderValidation.getList),orderController.getList);

router.route('/all').get(auth(),validate(orderValidation.getAll),orderController.getAll);

router
    .route('/:orderId')
    .get(validate(orderValidation.getOne), orderController.getOne)
    .patch(authIfHas(), addUpdatedByIdToBody, validate(orderValidation.updateOne), orderController.updateOne)
    .delete(authIfHas(), addDeletedByToBody, validate(orderValidation.deleteOne), orderController.deleteOne)

export const orderRoute = router;

