import express from 'express';
import { auth } from '../../middlewares/auth';
import {
    addCreatedByIdToBody,
    addDeletedByToBody,
    addUpdatedByIdToBody,
} from '../../middlewares/addUserToBody'
import {validate} from '../../middlewares/validate';
import {locationController} from './location.controller';
import {locationValidation} from './location.validation';

const router = express.Router()

router
    .route('/')
    .get(validate(locationValidation.getList),locationController.getList);

router.route('/all').get(auth(),validate(locationValidation.getAll),locationController.getAll);

router
    .route('/:locationId')
    .get(validate(locationValidation.getOne), locationController.getOne)
    .patch(auth(), addUpdatedByIdToBody, validate(locationValidation.updateOne), locationController.updateOne);

export const locationRoute = router;

