import express from 'express';
import { auth } from '../../middlewares/auth';
import {
    addCreatedByIdToBody,
    addDeletedByToBody,
    addUpdatedByIdToBody,
} from '../../middlewares/addUserToBody'
import {validate} from '../../middlewares/validate';
import {brandController} from './brand.controller';
import {brandValidation} from './brand.validation';

const router = express.Router()

router
    .route('/')
    .post(auth(),addCreatedByIdToBody,validate(brandValidation.createOne),brandController.createOne)
    .get(validate(brandValidation.getList),brandController.getList);

router.route('/all').get(auth(),validate(brandValidation.getAll),brandController.getAll);

router
    .route('/:brandId')
    .get(validate(brandValidation.getOne), brandController.getOne)
    .patch(auth(), addUpdatedByIdToBody, validate(brandValidation.updateOne), brandController.updateOne)
    .delete(auth(), addDeletedByToBody, validate(brandValidation.deleteOne), brandController.deleteOne)

export const brandRoute = router;

